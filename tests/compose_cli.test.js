test('nerve-compose init scaffolds nerve.json and workflow in cwd by default', async () => {
  const workspaceRoot = await mkdtemp(path.join(os.tmpdir(), 'nerve-compose-init-'))

  try {
    const result = await runProcess([
      path.join(process.cwd(), 'bin', 'nerve-compose.js'),
      'init',
      '--json',
    ], { cwd: workspaceRoot })
    assert.equal(result.code, 0)

    const payload = JSON.parse(result.stdout)
    assert.equal(payload.ok, true)
    assert.equal(payload.command, 'init')

    const nervePath = path.join(workspaceRoot, 'nerve.json')
    const workflowPath = path.join(workspaceRoot, 'workflow.nrv')
    const nerveRaw = await readFile(nervePath, 'utf8')
    const nerve = JSON.parse(nerveRaw)
    assert.equal(nerve.entrypointPath, 'workflow.nrv')
    assert.equal(nerve.baselineStatePath, 'state.init.json')
    assert.deepEqual(nerve.externals, ['user_message'])

    const baselineStateRaw = await readFile(path.join(workspaceRoot, 'state.init.json'), 'utf8')
    const baselineState = JSON.parse(baselineStateRaw)
    assert.deepEqual(baselineState, { count: 0 })

    const workflowRaw = await readFile(workflowPath, 'utf8')
    assert.equal(workflowRaw.includes('Generated by nerve-compose init'), true)
    assert.equal(workflowRaw.includes('on external "user_message"'), true)
    assert.equal(workflowRaw.includes('(no output yet)'), true)
    assert.equal(workflowRaw.includes('hello world!'), true)
  } finally {
    await rm(workspaceRoot, { recursive: true, force: true })
  }
})

