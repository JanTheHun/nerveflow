function normalizeHostProfileName(nameRaw) {
  const normalized = String(nameRaw ?? '').trim().toLowerCase()
  return normalized || HOST_PROFILE_COMPOSABLE
}

function isLikelyWorkspacePath(tokenRaw) {
  const token = String(tokenRaw ?? '').trim()
  if (!token) return false
  const candidateWorkspacePath = path.resolve(process.cwd(), token)
  return token === '.'
    || token === '..'
    || token.includes('/')
    || token.includes('\\')
    || /^[A-Za-z]:/.test(token)
    || existsSync(candidateWorkspacePath)
}

function stripMatchingQuotes(value) {
  const text = String(value ?? '')
  if ((text.startsWith('"') && text.endsWith('"')) || (text.startsWith("'") && text.endsWith("'"))) {
    return text.slice(1, -1)
  }
  return text
}

function loadWorkspaceEnvFile(workspaceDirRaw) {
  const workspaceDir = path.resolve(process.cwd(), String(workspaceDirRaw ?? ''))
  const envPath = path.resolve(workspaceDir, '.env')
  if (!existsSync(envPath)) return

  let content = ''
  try {
    content = readFileSync(envPath, 'utf8')
  } catch {
    return
  }

  for (const line of content.split(/\r?\n/)) {
    const trimmed = String(line ?? '').trim()
    if (!trimmed || trimmed.startsWith('#')) continue

    const cleaned = trimmed.startsWith('export ') ? trimmed.slice(7).trim() : trimmed
    const eqIndex = cleaned.indexOf('=')
    if (eqIndex <= 0) continue

    const key = cleaned.slice(0, eqIndex).trim()
    if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue
    if (Object.prototype.hasOwnProperty.call(process.env, key)) continue

    const valueRaw = cleaned.slice(eqIndex + 1)
    process.env[key] = stripMatchingQuotes(valueRaw)
  }
}

function parseCliOptions(argv) {
  const [subcommandRaw, ...rest] = argv
  const subcommand = String(subcommandRaw ?? '').trim().toLowerCase()
  if (!subcommand) {
    throw new Error('Usage: nerve-compose <modules|doctor|validate|add|init> [workspaceDir] [--json] [--builtin-only] [--strict]; add docs <minimal|ai> [workspaceDir]; add mcp [workspaceDir] [--inline]; add memory-pgvector [workspaceDir] [--blank]; add memory-spa [workspaceDir]; add semantic-surface [workspaceDir]; add host <composable> [workspaceDir]')
  }

  const options = {
    subcommand,
    capability: '',
    addName: '',
    mcpInline: false,
    docsProfile: '',
    withAgentInstructions: false,
    noPrompts: false,
    instructionsOnly: false,
    blank: false,
    modelTransport: '',
    workspaceDir: null,
    json: false,
    builtinOnly: false,
    strict: false,
  }

  for (let i = 0; i < rest.length; i += 1) {
    const token = String(rest[i] ?? '').trim()
    if (!token) continue

    if (token === '--json') {
      options.json = true
      continue
    }

    if (token === '--builtin-only') {
      options.builtinOnly = true
      continue
    }

    if (token === '--strict') {
      options.strict = true
      continue
    }

    if (token === '--inline') {
      options.mcpInline = true
      continue
    }

    if (token === '--with-agent-instructions') {
      options.withAgentInstructions = true
      continue
    }

    if (token === '--no-prompts') {
      options.noPrompts = true
      continue
    }

    if (token === '--instructions-only') {
      options.instructionsOnly = true
      continue
    }

    if (token === '--blank') {
      options.blank = true
      continue
    }

    if (token === '--transport') {
      const value = String(rest[i + 1] ?? '').trim()
      if (!value || value.startsWith('-')) {
        throw new Error('--transport requires a value')
      }
      options.modelTransport = value
      i += 1
      continue
    }

    if (token.startsWith('-')) {
      throw new Error(`Unknown argument: ${token}`)
    }

    if (options.subcommand === 'add' && !options.capability) {
      options.capability = token
      continue
    }

    if (options.subcommand === 'add' && options.capability === 'mcp' && !options.workspaceDir) {
      const nextToken = String(rest[i + 1] ?? '').trim()
      const hasFollowingPositional = Boolean(nextToken && !nextToken.startsWith('-'))
      const looksLikeWorkspace = isLikelyWorkspacePath(token)

      if (!hasFollowingPositional && looksLikeWorkspace) {
        options.workspaceDir = token
        continue
      }

      throw new Error(`Unexpected extra argument: ${token}`)
    }

    if (options.subcommand === 'add' && options.capability === 'host' && !options.addName && !options.workspaceDir) {
      const normalizedToken = normalizeHostProfileName(token)
      if (SUPPORTED_HOST_PROFILES.has(normalizedToken)) {
        options.addName = normalizedToken
        continue
      }

      const nextToken = String(rest[i + 1] ?? '').trim()
      const hasFollowingPositional = Boolean(nextToken && !nextToken.startsWith('-'))
      const looksLikeWorkspace = isLikelyWorkspacePath(token)
      if (!hasFollowingPositional && looksLikeWorkspace) {
        options.addName = HOST_PROFILE_COMPOSABLE
        options.workspaceDir = token
        continue
      }

      throw new Error(`Unsupported host profile: ${token}`)
    }

    if (options.subcommand === 'add' && (options.capability === 'transport' || options.capability === 'model') && !options.addName) {
      options.addName = token
      continue
    }

    if (options.subcommand === 'add' && options.capability === 'docs' && !options.docsProfile) {
      options.docsProfile = token
      continue
    }

    if (options.workspaceDir) {
      throw new Error(`Unexpected extra argument: ${token}`)
    }

    options.workspaceDir = token
  }

  if (
    options.subcommand !== 'modules'
    && options.subcommand !== 'doctor'
    && options.subcommand !== 'validate'
    && options.subcommand !== 'add'
    && options.subcommand !== 'init'
  ) {
    throw new Error(`Unknown subcommand: ${options.subcommand}`)
  }

  if (options.subcommand === 'add' && !options.capability) {
    throw new Error('add requires <capability>')
  }

  if (options.subcommand === 'add' && (options.capability === 'transport' || options.capability === 'model') && !options.addName) {
    throw new Error(`add ${options.capability} requires <name>`)
  }

  if (options.subcommand === 'add' && options.capability === 'docs' && !options.docsProfile) {
    throw new Error('add docs requires <profile>')
  }

  if (options.subcommand === 'add' && options.capability === 'docs' && !VALID_DOCS_PROFILES.has(options.docsProfile)) {
    throw new Error('add docs profile must be one of: minimal, ai')
  }

  if (options.subcommand === 'add' && options.capability === 'host' && !SUPPORTED_HOST_PROFILES.has(normalizeHostProfileName(options.addName))) {
    throw new Error(`Unsupported host profile: ${options.addName}`)
  }

  if (options.subcommand === 'add' && options.capability === 'mcp' && options.addName) {
    throw new Error('add mcp does not accept a server name; configure host MCP servers directly')
  }

  if (options.mcpInline && !(options.subcommand === 'add' && options.capability === 'mcp')) {
    throw new Error('--inline is only valid for add mcp')
  }

  if (options.blank && !(options.subcommand === 'add' && options.capability === 'memory-pgvector')) {
    throw new Error('--blank is only valid for add memory-pgvector')
  }

  if (options.subcommand === 'add' && options.capability === 'model' && !options.modelTransport) {
    throw new Error('add model requires --transport <transportName>')
  }

  if (options.modelTransport && options.subcommand !== 'add') {
    throw new Error('--transport is only valid for add model')
  }

  if (options.modelTransport && options.subcommand === 'add' && options.capability !== 'model') {
    throw new Error('--transport is only valid for add model')
  }

  if (options.subcommand !== 'modules' && options.builtinOnly) {
    throw new Error('--builtin-only is only valid for modules')
  }

  if (options.subcommand !== 'doctor' && options.strict) {
    throw new Error('--strict is only valid for doctor')
  }

  if (
    (options.withAgentInstructions || options.noPrompts || options.instructionsOnly)
    && !(options.subcommand === 'add' && options.capability === 'docs' && options.docsProfile === DOCS_PROFILE_AI)
  ) {
    throw new Error('--with-agent-instructions, --no-prompts, and --instructions-only are only valid for add docs ai')
  }

  return options
}

function printValidateSummary(payload) {
  console.log(`workspace: ${payload.workspaceDir}`)
  console.log(`summary: capabilities=${payload.summary.capabilities}, tools=${payload.summary.toolProviders}, connectors=${payload.summary.ingressConnectors}, realizers=${payload.summary.effectRealizers}`)
  if (Array.isArray(payload.resolvedCapabilities) && payload.resolvedCapabilities.length > 0) {
    for (const entry of payload.resolvedCapabilities) {
      console.log(`resolved capability=${entry.capabilityName} module=${entry.moduleName} provider=${entry.provider} mode=${entry.mode}`)
    }
  }
  if (payload.error) {
    console.log(`ERROR validate ${payload.error}`)
  }
}

async function runValidateCommand(options) {
  const cwd = process.cwd()
  const workspaceDirAbsolutePath = options.workspaceDir
    ? path.resolve(cwd, options.workspaceDir)
    : PACKAGE_ROOT

  loadWorkspaceEnvFile(workspaceDirAbsolutePath)

  let summary = null
  let error = null

  try {
    const workspaceDirRelativePath = options.workspaceDir
      ? toRelativePath(workspaceDirAbsolutePath, PACKAGE_ROOT)
      : '.'
    const host = createComposableHost({
      workspaceDir: workspaceDirRelativePath,
      autoAttachCapabilitiesFromWorkspace: true,
    })
    summary = await host.validateWorkspaceCapabilities()
  } catch (err) {
    error = err
  }

  const payload = {
    ok: !error,
    command: 'validate',
    workspaceDir: options.workspaceDir
      ? toRelativePath(workspaceDirAbsolutePath, PACKAGE_ROOT)
      : '.',
    summary: summary || {
      capabilities: 0,
      toolProviders: 0,
      ingressConnectors: 0,
      effectRealizers: 0,
    },
    resolvedCapabilities: Array.isArray(summary?.workspaceCapabilities) ? summary.workspaceCapabilities : [],
    error: error ? String(error?.message ?? error) : null,
  }

  if (options.json) {
    console.log(JSON.stringify(payload, null, 2))
  } else {
    printValidateSummary(payload)
  }

  return { ok: payload.ok }
}

function buildInitPayload({ workspaceDir, fileResults }) {
  const blocking = fileResults.filter((result) => result.action === 'skipped_manual_merge')
  return {
    ok: blocking.length === 0,
    command: 'init',
    workspaceDir,
    files: fileResults,
  }
}

function printInitSummary(payload) {
  console.log(`workspace: ${payload.workspaceDir}`)
  for (const result of payload.files) {
    const message = result.message ? ` message=${result.message}` : ''
    console.log(`file=${result.path} action=${result.action}${message}`)
  }
}

async function runInitCommand(options) {
  const cwd = process.cwd()
  const workspaceDir = options.workspaceDir
    ? path.resolve(cwd, options.workspaceDir)
    : cwd

  const nerveConfigPath = path.join(workspaceDir, 'nerve.json')
  const nextvConfigPath = path.join(workspaceDir, 'nextv.json')
  const fileResults = []

  let entrypointPath = 'workflow.nrv'
  const hasNerveConfig = existsSync(nerveConfigPath)
  const hasNextvConfig = existsSync(nextvConfigPath)

  if (hasNerveConfig) {
    entrypointPath = resolveEntrypointPathFromConfig(nerveConfigPath, 'workflow.nrv')
    fileResults.push({
      action: 'unchanged',
      path: nerveConfigPath,
      message: hasNextvConfig
        ? 'nerve.json already exists; runtime prefers nerve.json when both config files are present'
        : 'nerve.json already exists',
    })
  } else if (hasNextvConfig) {
    fileResults.push({
      action: 'skipped',
      path: nerveConfigPath,
      message: 'nextv.json already exists; init does not auto-migrate to nerve.json',
    })
  } else {
    ensureParentDir(nerveConfigPath)
    writeFileSync(nerveConfigPath, buildInitConfigSource('workflow.nrv'), 'utf8')
    fileResults.push({ action: 'created', path: nerveConfigPath })
  }

  if (!hasNextvConfig || hasNerveConfig) {
    const workflowPath = path.resolve(workspaceDir, entrypointPath)
    const baselineStatePath = path.resolve(workspaceDir, 'state.init.json')
    if (existsSync(workflowPath)) {
      fileResults.push({ action: 'unchanged', path: workflowPath })
    } else {
      ensureParentDir(workflowPath)
      writeFileSync(workflowPath, buildGeneratedWorkflowSource(), 'utf8')
      fileResults.push({ action: 'created', path: workflowPath })
    }

    if (existsSync(baselineStatePath)) {
      fileResults.push({ action: 'unchanged', path: baselineStatePath })
    } else {
      ensureParentDir(baselineStatePath)
      writeFileSync(baselineStatePath, buildInitBaselineStateSource(), 'utf8')
      fileResults.push({ action: 'created', path: baselineStatePath })
    }
  }

  const relativeFileResults = fileResults.map((result) => ({
    ...result,
    path: toRelativePath(result.path, cwd),
  }))

  const payload = buildInitPayload({
    workspaceDir: toRelativePath(workspaceDir, cwd),
    fileResults: relativeFileResults,
  })

  if (options.json) {
    console.log(JSON.stringify(payload, null, 2))
  } else {
    printInitSummary(payload)
  }

  return { ok: payload.ok }
}

function ensureParentDir(filePath) {
  mkdirSync(path.dirname(filePath), { recursive: true })
}

function readTextFileIfExists(filePath) {
  if (!existsSync(filePath)) return null
  return readFileSync(filePath, 'utf8')
}

function normalizeTrailingNewline(text) {
  return text.endsWith('\n') ? text : `${text}\n`
}

function readJsonObjectFile(filePath) {
  if (!existsSync(filePath)) {
    return { exists: false, parsed: null, error: null }
  }

  try {
    const parsed = JSON.parse(readFileSync(filePath, 'utf8'))
    if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
      return { exists: true, parsed: null, error: 'must be an object' }
    }
    return { exists: true, parsed, error: null }
  } catch (error) {
    return {
      exists: true,
      parsed: null,
      error: error instanceof Error ? error.message : String(error),
    }
  }
}

function extractRegistryMap(raw, registryName) {
  if (raw && typeof raw === 'object' && !Array.isArray(raw)) {
    const wrappedMap = raw[registryName]
    if (wrappedMap && typeof wrappedMap === 'object' && !Array.isArray(wrappedMap)) {
      return wrappedMap
    }
  }

  return raw
}

function upsertRegistryEntryByName(filePath, configLabel, registryName, entryName, desiredEntry, options = {}) {
  const { writeWrapped = false } = options
  const existing = readJsonObjectFile(filePath)

  if (!existing.exists) {
    const createdMap = { [entryName]: desiredEntry }
    const nextValue = writeWrapped ? { [registryName]: createdMap } : createdMap
    ensureParentDir(filePath)
    writeFileSync(filePath, `${JSON.stringify(nextValue, null, 2)}\n`, 'utf8')
    return { action: 'created', path: filePath }
  }

  if (existing.error) {
    return { action: 'skipped', path: filePath, message: `${configLabel} must be an object; ${registryName} not added` }
  }

  const currentMap = extractRegistryMap(existing.parsed, registryName)
  const map = currentMap && typeof currentMap === 'object' && !Array.isArray(currentMap)
    ? { ...currentMap }
    : {}

  if (JSON.stringify(map[entryName] ?? null) === JSON.stringify(desiredEntry)) {
    return { action: 'unchanged', path: filePath }
  }

  map[entryName] = desiredEntry
  const nextValue = writeWrapped ? { ...existing.parsed, [registryName]: map } : map
  writeFileSync(filePath, `${JSON.stringify(nextValue, null, 2)}\n`, 'utf8')
  return { action: 'updated', path: filePath }
}

function resolveRegistryWriteTarget(workspaceDir, configPath, configLabel, registryName) {
  const standalonePath = path.join(workspaceDir, `${registryName}.json`)

  const rootConfig = readJsonObjectFile(configPath)
  if (!rootConfig.exists) {
    return {
      action: 'skipped',
      path: configPath,
      message: `${configLabel} not found; ${registryName} not added`,
    }
  }

  if (rootConfig.error) {
    return {
      action: 'skipped',
      path: configPath,
      message: `${configLabel} must be an object; ${registryName} not added`,
    }
  }

  const inlineMap = rootConfig.parsed[registryName]
  if (inlineMap && typeof inlineMap === 'object' && !Array.isArray(inlineMap)) {
    return {
      action: 'inline',
      path: configPath,
      label: configLabel,
      registryName,
      writeWrapped: true,
    }
  }

  const standalone = readJsonObjectFile(standalonePath)
  if (standalone.error) {
    return {
      action: 'skipped',
      path: standalonePath,
      message: `${registryName}.json must be an object; ${registryName} not added`,
    }
  }

  return {
    action: 'standalone',
    path: standalonePath,
    label: `${registryName}.json`,
    registryName,
    writeWrapped: false,
  }
}

function upsertEnvExampleLines(envExamplePath, envLines) {
  const existing = readTextFileIfExists(envExamplePath)
  if (existing == null) {
    ensureParentDir(envExamplePath)
    writeFileSync(envExamplePath, `${envLines.join('\n')}\n`, 'utf8')
    return { action: 'created', path: envExamplePath }
  }

  const currentLines = existing.split(/\r?\n/)
  const existingSet = new Set(currentLines.map((line) => String(line ?? '').trim()))
  const linesToAppend = envLines.filter((line) => !existingSet.has(line.trim()))
  if (linesToAppend.length === 0) {
    return { action: 'unchanged', path: envExamplePath }
  }

  const separator = existing.trim() ? '\n' : ''
  const nextContent = `${normalizeTrailingNewline(existing).trimEnd()}${separator}\n${linesToAppend.join('\n')}\n`
    .replace(/^\n/, '')
  writeFileSync(envExamplePath, nextContent, 'utf8')
  return { action: 'updated', path: envExamplePath }
}

