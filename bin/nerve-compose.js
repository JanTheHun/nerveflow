async function runAddCommand(options) {
  const cwd = process.cwd()
  const workspaceDir = options.workspaceDir
    ? path.resolve(cwd, options.workspaceDir)
    : cwd

  let fileResults = []
  if (options.capability === 'memory-pgvector') {
    const memoryRuntimeEnvLines = options.blank ? MEMORY_RUNTIME_ENV_LINES_BLANK : MEMORY_RUNTIME_ENV_LINES
    fileResults = [
      upsertGeneratedHostModules(workspaceDir),
      scaffoldMemorySetupHelper(workspaceDir),
      scaffoldMemoryHealthHelper(workspaceDir),
      upsertEnvExample(path.join(workspaceDir, '.env.example')),
      upsertEnvLines(path.join(workspaceDir, '.env'), memoryRuntimeEnvLines),
      upsertNextVCapabilityConfig(workspaceDir),
    ]
  } else if (options.capability === 'memory-spa') {
    const prerequisite = verifyMemorySpaPrerequisite(workspaceDir)
    fileResults = prerequisite.ok
      ? scaffoldMemorySpa(workspaceDir)
      : [{ action: 'skipped', path: prerequisite.path, message: prerequisite.message }]
  } else if (options.capability === 'speech') {
    fileResults = [
      upsertGeneratedSpeechHostModules(workspaceDir),
      upsertNextVSpeechCapabilityConfig(workspaceDir),
      scaffoldSpeechSurface(workspaceDir),
    ]
  } else if (options.capability === 'semantic-surface') {
    fileResults = [
      upsertGeneratedSemanticSurfaceHostModules(workspaceDir),
      upsertNextVSemanticSurfaceCapabilityConfig(workspaceDir),
      scaffoldSemanticSurface(workspaceDir),
    ]
  } else if (options.capability === 'mcp') {
    const serverName = 'local-mcp'
    const serverRelativePath = './capabilities/mcp/servers/local-mcp.mjs'
    const scaffoldResult = scaffoldMcpServer(workspaceDir)
    if (options.mcpInline) {
      fileResults = [
        scaffoldResult,
        upsertNextVMcpCapabilityConfig(workspaceDir, {
          serverName,
          serverRelativePath,
          inline: true,
        }),
      ]
    } else {
      fileResults = [
        scaffoldResult,
        scaffoldMcpConfig(workspaceDir, serverName, serverRelativePath, DEFAULT_MCP_CONFIG_PATH),
        upsertNextVMcpCapabilityConfig(workspaceDir, {
          serverName,
          serverRelativePath,
          inline: false,
          configPath: DEFAULT_MCP_CONFIG_PATH,
        }),
      ]
    }
  } else if (options.capability === 'transport') {
    const { path: configPath, label: configLabel } = resolveWorkspaceConfigPath(workspaceDir)
    const transportEnvLines = buildTransportEnvLines(options.addName)
    const transportTarget = resolveRegistryWriteTarget(workspaceDir, configPath, configLabel, 'transports')
    const transportConfigResult = transportTarget.action === 'skipped'
      ? transportTarget
      : upsertNextVTransportConfigByName(
        transportTarget.path,
        transportTarget.label,
        options.addName,
        buildTransportConfig(options.addName),
        transportTarget,
      )

    fileResults = [transportConfigResult]
    if (transportConfigResult.action !== 'skipped') {
      fileResults.push(
        upsertEnvExampleLines(path.join(workspaceDir, '.env.example'), transportEnvLines),
        upsertEnvLines(path.join(workspaceDir, '.env'), transportEnvLines),
      )
    }
  } else if (options.capability === 'model') {
    loadWorkspaceEnvFile(workspaceDir)
    const { path: configPath, label: configLabel } = resolveWorkspaceConfigPath(workspaceDir)
    const resolvedModel = await resolveExactModelNameForAdd({
      workspaceDir,
      configPath,
      configLabel,
      modelName: options.addName,
      transportName: options.modelTransport,
    })

    if (!resolvedModel.ok) {
      fileResults = [
        {
          action: 'skipped',
          path: configPath,
          message: resolvedModel.message,
        },
      ]
    } else {
      const modelTarget = resolveRegistryWriteTarget(workspaceDir, configPath, configLabel, 'models')
      if (modelTarget.action === 'skipped') {
        fileResults = [modelTarget]
      } else {
        const modelResult = upsertNextVModelConfigByName(
          modelTarget.path,
          modelTarget.label,
          resolvedModel.modelName,
          options.modelTransport,
          buildModelConfig(resolvedModel.modelName, options.modelTransport),
          {
            ...modelTarget,
            workspaceDir,
            transportLookupPath: configPath,
            transportLookupLabel: configLabel,
            transportMap: readConfiguredTransportsMapForWorkspace(workspaceDir, configPath),
          },
        )
        if (resolvedModel.message && modelResult.action !== 'skipped') {
          modelResult.message = resolvedModel.message
        }
        fileResults = [modelResult]
        if (resolvedModel.modelName !== options.addName) {
          options.addName = resolvedModel.modelName
        }
      }
    }
  } else if (options.capability === 'docs') {
    let scaffoldInstructions = false
    if (options.docsProfile === DOCS_PROFILE_AI) {
      scaffoldInstructions = await shouldScaffoldAiInstructions(options)
    }

    let includeEcosystemFeedback = false
    if (options.docsProfile === DOCS_PROFILE_AI && !options.instructionsOnly) {
      includeEcosystemFeedback = await shouldIncludeEcosystemFeedbackSuggestions(options)
    }

    if (!options.instructionsOnly) {
      fileResults = addDocsByProfile(workspaceDir, options.docsProfile, {
        includeEcosystemFeedback,
      })
    }

    if (options.docsProfile === DOCS_PROFILE_AI) {
      if (scaffoldInstructions) {
        fileResults.push(...scaffoldAiInstructionFiles(workspaceDir))
      } else if (options.instructionsOnly) {
        fileResults.push({
          action: 'skipped',
          path: workspaceDir,
          message: 'instruction scaffolding not enabled; use --with-agent-instructions or allow interactive prompt',
        })
      }
    }
  } else if (options.capability === 'host') {
    fileResults = scaffoldReferenceHost(workspaceDir, options.addName)
    options.addName = normalizeHostProfileName(options.addName)
  } else {
    throw new Error(`Unsupported capability: ${options.capability}`)
  }

  fileResults = fileResults.map((result) => ({
    ...result,
    path: toRelativePath(result.path, cwd),
  }))

  const payload = buildAddPayload({
    capability: options.capability === 'transport' || options.capability === 'model' || options.capability === 'host'
      ? `${options.capability}:${options.addName}`
      : options.capability === 'mcp'
        ? 'mcp'
      : options.capability === 'docs'
        ? `docs:${options.docsProfile}`
      : options.capability,
    workspaceDir: toRelativePath(workspaceDir, cwd),
    fileResults,
  })

  payload.composable = buildComposableGuidance(options)

  const hasSkipError = payload.files.some((entry) => entry.action === 'skipped' && String(entry.message ?? '').includes('transport "'))
  const hasModelResolutionError = payload.files.some((entry) => entry.action === 'skipped' && String(entry.message ?? '').includes('model "'))
  const hasMemorySpaError = options.capability === 'memory-spa'
    && payload.files.some((entry) => entry.action === 'skipped')
  if (hasSkipError || hasModelResolutionError || hasMemorySpaError) payload.ok = false

  if (options.json) {
    console.log(JSON.stringify(payload, null, 2))
  } else {
    printAddSummary(payload)
  }

  return { ok: payload.ok }
}

function toRelativePath(targetPath, cwd) {
  if (!targetPath) return null
  const relativePath = path.relative(cwd, targetPath)
  if (!relativePath || relativePath.startsWith('..')) return targetPath
  return relativePath
}

