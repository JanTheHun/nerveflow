test('nerve-compose add mcp is idempotent on rerun', async () => {
  const workspaceRoot = await mkdtemp(path.join(process.cwd(), '.tmp-compose-mcp-'))
  const workspaceRelativePath = path.relative(process.cwd(), workspaceRoot).replace(/\\/g, '/')

  await writeFile(path.join(workspaceRoot, 'nextv.json'), JSON.stringify({ entrypointPath: 'entry.nrv' }, null, 2), 'utf8')

  try {
    const first = await runProcess(['bin/nerve-compose.js', 'add', 'mcp', workspaceRelativePath, '--json'])
    assert.equal(first.code, 0)

    const second = await runProcess(['bin/nerve-compose.js', 'add', 'mcp', workspaceRelativePath, '--json'])
    assert.equal(second.code, 0)

    const payload = JSON.parse(second.stdout)
    assert.equal(payload.ok, true)
    assert.equal(payload.capability, 'mcp')
    const actions = payload.files.map((entry) => entry.action)
    assert.equal(actions.every((action) => action === 'unchanged' || action === 'updated' || action === 'skipped'), true)
  } finally {
    await rm(workspaceRoot, { recursive: true, force: true })
  }
})
