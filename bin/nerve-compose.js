function normalizeHostProfileName(nameRaw) {
  const normalized = String(nameRaw ?? '').trim().toLowerCase()
  return normalized || HOST_PROFILE_COMPOSABLE
}

function isLikelyWorkspacePath(tokenRaw) {
  const token = String(tokenRaw ?? '').trim()
  if (!token) return false
  const candidateWorkspacePath = path.resolve(process.cwd(), token)
  return token === '.'
    || token === '..'
    || token.includes('/')
    || token.includes('\\')
    || /^[A-Za-z]:/.test(token)
    || existsSync(candidateWorkspacePath)
}

function stripMatchingQuotes(value) {
  const text = String(value ?? '')
  if ((text.startsWith('"') && text.endsWith('"')) || (text.startsWith("'") && text.endsWith("'"))) {
    return text.slice(1, -1)
  }
  return text
}

function loadWorkspaceEnvFile(workspaceDirRaw) {
  const workspaceDir = path.resolve(process.cwd(), String(workspaceDirRaw ?? ''))
  const envPath = path.resolve(workspaceDir, '.env')
  if (!existsSync(envPath)) return

  let content = ''
  try {
    content = readFileSync(envPath, 'utf8')
  } catch {
    return
  }

  for (const line of content.split(/\r?\n/)) {
    const trimmed = String(line ?? '').trim()
    if (!trimmed || trimmed.startsWith('#')) continue

    const cleaned = trimmed.startsWith('export ') ? trimmed.slice(7).trim() : trimmed
    const eqIndex = cleaned.indexOf('=')
    if (eqIndex <= 0) continue

    const key = cleaned.slice(0, eqIndex).trim()
    if (!/^[A-Za-z_][A-Za-z0-9_]*$/.test(key)) continue
    if (Object.prototype.hasOwnProperty.call(process.env, key)) continue

    const valueRaw = cleaned.slice(eqIndex + 1)
    process.env[key] = stripMatchingQuotes(valueRaw)
  }
}

function parseCliOptions(argv) {
  const [subcommandRaw, ...rest] = argv
  const subcommand = String(subcommandRaw ?? '').trim().toLowerCase()
  if (!subcommand) {
    throw new Error('Usage: nerve-compose <modules|doctor|validate|add|init> [workspaceDir] [--json] [--builtin-only] [--strict]; add docs <minimal|ai> [workspaceDir]; add mcp [serverName] [workspaceDir]; add host <composable> [workspaceDir]')
  }

  const options = {
    subcommand,
    capability: '',
    addName: '',
    docsProfile: '',
    withAgentInstructions: false,
    noPrompts: false,
    instructionsOnly: false,
    modelTransport: '',
    workspaceDir: null,
    json: false,
    builtinOnly: false,
    strict: false,
  }

  for (let i = 0; i < rest.length; i += 1) {
    const token = String(rest[i] ?? '').trim()
    if (!token) continue

    if (token === '--json') {
      options.json = true
      continue
    }

    if (token === '--builtin-only') {
      options.builtinOnly = true
      continue
    }

    if (token === '--strict') {
      options.strict = true
      continue
    }

    if (token === '--with-agent-instructions') {
      options.withAgentInstructions = true
      continue
    }

    if (token === '--no-prompts') {
      options.noPrompts = true
      continue
    }

    if (token === '--instructions-only') {
      options.instructionsOnly = true
      continue
    }

    if (token === '--transport') {
      const value = String(rest[i + 1] ?? '').trim()
      if (!value || value.startsWith('-')) {
        throw new Error('--transport requires a value')
      }
      options.modelTransport = value
      i += 1
      continue
    }

    if (token.startsWith('-')) {
      throw new Error(`Unknown argument: ${token}`)
    }

    if (options.subcommand === 'add' && !options.capability) {
      options.capability = token
      continue
    }

    if (options.subcommand === 'add' && options.capability === 'mcp' && !options.workspaceDir) {
      const nextToken = String(rest[i + 1] ?? '').trim()
      const hasFollowingPositional = Boolean(nextToken && !nextToken.startsWith('-'))
      const looksLikeWorkspace = isLikelyWorkspacePath(token)

      if (!hasFollowingPositional && looksLikeWorkspace) {
        options.workspaceDir = token
        continue
      }

      throw new Error(`Unexpected extra argument: ${token}`)
    }

    if (options.subcommand === 'add' && options.capability === 'host' && !options.addName && !options.workspaceDir) {
      const normalizedToken = normalizeHostProfileName(token)
      if (SUPPORTED_HOST_PROFILES.has(normalizedToken)) {
        options.addName = normalizedToken
        continue
      }

      const nextToken = String(rest[i + 1] ?? '').trim()
      const hasFollowingPositional = Boolean(nextToken && !nextToken.startsWith('-'))
      const looksLikeWorkspace = isLikelyWorkspacePath(token)
      if (!hasFollowingPositional && looksLikeWorkspace) {
        options.addName = HOST_PROFILE_COMPOSABLE
        options.workspaceDir = token
        continue
      }

      throw new Error(`Unsupported host profile: ${token}`)
    }

    if (options.subcommand === 'add' && (options.capability === 'transport' || options.capability === 'model') && !options.addName) {
      options.addName = token
      continue
    }

    if (options.subcommand === 'add' && options.capability === 'docs' && !options.docsProfile) {
      options.docsProfile = token
      continue
    }

    if (options.workspaceDir) {
      throw new Error(`Unexpected extra argument: ${token}`)
    }

    options.workspaceDir = token
  }

  if (
    options.subcommand !== 'modules'
    && options.subcommand !== 'doctor'
    && options.subcommand !== 'validate'
    && options.subcommand !== 'add'
    && options.subcommand !== 'init'
  ) {
    throw new Error(`Unknown subcommand: ${options.subcommand}`)
  }

  if (options.subcommand === 'add' && !options.capability) {
    throw new Error('add requires <capability>')
  }

  if (options.subcommand === 'add' && (options.capability === 'transport' || options.capability === 'model') && !options.addName) {
    throw new Error(`add ${options.capability} requires <name>`)
  }

  if (options.subcommand === 'add' && options.capability === 'docs' && !options.docsProfile) {
    throw new Error('add docs requires <profile>')
  }

  if (options.subcommand === 'add' && options.capability === 'docs' && !VALID_DOCS_PROFILES.has(options.docsProfile)) {
    throw new Error('add docs profile must be one of: minimal, ai')
  }

  if (options.subcommand === 'add' && options.capability === 'host' && !SUPPORTED_HOST_PROFILES.has(normalizeHostProfileName(options.addName))) {
    throw new Error(`Unsupported host profile: ${options.addName}`)
  }

  if (options.subcommand === 'add' && options.capability === 'mcp' && options.addName) {
    throw new Error('add mcp does not accept a server name; configure host MCP servers directly')
  }

  if (options.subcommand === 'add' && options.capability === 'model' && !options.modelTransport) {
    throw new Error('add model requires --transport <transportName>')
  }

  if (options.modelTransport && options.subcommand !== 'add') {
    throw new Error('--transport is only valid for add model')
  }

  if (options.modelTransport && options.subcommand === 'add' && options.capability !== 'model') {
    throw new Error('--transport is only valid for add model')
  }

  if (options.subcommand !== 'modules' && options.builtinOnly) {
    throw new Error('--builtin-only is only valid for modules')
  }

  if (options.subcommand !== 'doctor' && options.strict) {
    throw new Error('--strict is only valid for doctor')
  }

  if (
    (options.withAgentInstructions || options.noPrompts || options.instructionsOnly)
    && !(options.subcommand === 'add' && options.capability === 'docs' && options.docsProfile === DOCS_PROFILE_AI)
  ) {
    throw new Error('--with-agent-instructions, --no-prompts, and --instructions-only are only valid for add docs ai')
  }

  return options
}

