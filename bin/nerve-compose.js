function collectFilesRecursive(sourceDir) {
  const files = []
  const stack = ['']

  while (stack.length > 0) {
    const current = stack.pop()
    const absoluteDir = path.join(sourceDir, current)
    const entries = readdirSync(absoluteDir, { withFileTypes: true })

    for (const entry of entries) {
      const relativePath = current ? path.join(current, entry.name) : entry.name
      if (entry.isDirectory()) {
        stack.push(relativePath)
        continue
      }
      if (entry.isFile()) {
        files.push(relativePath)
      }
    }
  }

  return files.sort()
}

function copyFileNonDestructive(sourcePath, targetPath) {
  const sourceContent = readFileSync(sourcePath, 'utf8')
  const existing = readTextFileIfExists(targetPath)

  if (existing == null) {
    ensureParentDir(targetPath)
    writeFileSync(targetPath, sourceContent, 'utf8')
    return { action: 'created', path: targetPath }
  }

  if (existing === sourceContent) {
    return { action: 'unchanged', path: targetPath }
  }

  return {
    action: 'skipped_manual_merge',
    path: targetPath,
    message: 'target file already exists with different content; manual merge required',
  }
}

function copyDirectoryTreeNonDestructive(sourceDir, targetDir) {
  if (!existsSync(sourceDir)) {
    return [{
      action: 'skipped',
      path: targetDir,
      message: `source directory not found: ${sourceDir}`,
    }]
  }

  const sourceFiles = collectFilesRecursive(sourceDir)
  const results = []
  for (const relativeFilePath of sourceFiles) {
    const sourcePath = path.join(sourceDir, relativeFilePath)
    const targetPath = path.join(targetDir, relativeFilePath)
    results.push(copyFileNonDestructive(sourcePath, targetPath))
  }
  return results
}

function isCiEnvironment() {
  return String(process.env.CI ?? '').trim().toLowerCase() === 'true'
    || String(process.env.GITHUB_ACTIONS ?? '').trim().toLowerCase() === 'true'
}

function canUseInteractivePrompt() {
  return Boolean(process.stdin?.isTTY && process.stdout?.isTTY && !isCiEnvironment())
}

async function promptYesNo(question) {
  const rl = createInterface({
    input: process.stdin,
    output: process.stdout,
  })

  try {
    const answer = String(await rl.question(`\n${question}\n\n[y/N] `)).trim().toLowerCase()
    const accepted = answer === 'y' || answer === 'yes'
    console.log('')
    return accepted
  } finally {
    rl.close()
  }
}

