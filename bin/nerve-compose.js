function resolveEntrypointPathFromConfig(configPath, fallbackEntrypointPath = 'workflow.nrv') {
  const existing = readTextFileIfExists(configPath)
  if (existing == null) return fallbackEntrypointPath

  try {
    const parsed = JSON.parse(existing)
    const configuredEntrypoint = String(parsed?.entrypointPath ?? '').trim()
    return configuredEntrypoint || fallbackEntrypointPath
  } catch {
    return fallbackEntrypointPath
  }
}

