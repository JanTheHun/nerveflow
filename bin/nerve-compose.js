function upsertManagedAiInstructions(filePath) {
  const existing = readTextFileIfExists(filePath)
  const block = buildManagedAiInstructionsBlock()

  if (existing == null) {
    ensureParentDir(filePath)
    writeFileSync(filePath, `${block}\n`, 'utf8')
    return { action: 'created', path: filePath }
  }

  const beginCount = (existing.match(new RegExp(AI_INSTRUCTIONS_BEGIN_MARKER.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')) || []).length
  const endCount = (existing.match(new RegExp(AI_INSTRUCTIONS_END_MARKER.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'g')) || []).length

  if (beginCount !== endCount || beginCount > 1 || endCount > 1) {
    return {
      action: 'skipped_manual_merge',
      path: filePath,
      message: 'instruction markers are malformed or duplicated; manual merge required',
    }
  }

  if (beginCount === 0) {
    const prefix = existing.trim() ? `${normalizeTrailingNewline(existing).trimEnd()}\n\n` : ''
    const next = `${prefix}${block}\n`
    if (next === normalizeTrailingNewline(existing)) {
      return { action: 'unchanged', path: filePath }
    }

    writeFileSync(filePath, next, 'utf8')
    return { action: 'updated', path: filePath }
  }

  const beginIndex = existing.indexOf(AI_INSTRUCTIONS_BEGIN_MARKER)
  const endIndex = existing.indexOf(AI_INSTRUCTIONS_END_MARKER, beginIndex)
  if (beginIndex < 0 || endIndex < 0 || endIndex < beginIndex) {
    return {
      action: 'skipped_manual_merge',
      path: filePath,
      message: 'instruction markers are malformed; manual merge required',
    }
  }

  const currentBlock = existing.slice(beginIndex, endIndex + AI_INSTRUCTIONS_END_MARKER.length)
  if (currentBlock === block) {
    return { action: 'unchanged', path: filePath }
  }

  const next = `${existing.slice(0, beginIndex)}${block}${existing.slice(endIndex + AI_INSTRUCTIONS_END_MARKER.length)}`
  writeFileSync(filePath, next, 'utf8')
  return { action: 'updated', path: filePath }
}

