function buildTransportEnvLines(transportName) {
  const normalized = String(transportName ?? '').trim().toLowerCase()
  if (Object.prototype.hasOwnProperty.call(TRANSPORT_ENV_TEMPLATES, normalized)) {
    return TRANSPORT_ENV_TEMPLATES[normalized]
  }
  const keyPrefix = normalized.replace(/[^a-z0-9]+/gi, '_').toUpperCase() || 'TRANSPORT'
  return [
    `# transport: ${transportName}`,
    `${keyPrefix}_BASE_URL=http://127.0.0.1:11434`,
  ]
}

