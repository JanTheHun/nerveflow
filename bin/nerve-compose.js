function upsertNextVCapabilityConfigByName(configPath, configLabel, capabilityName, desiredRequire, desiredModule) {
  if (!existsSync(configPath)) {
    return { action: 'skipped', path: configPath, message: `${configLabel} not found; capability declarations not added` }
  }

  const raw = readFileSync(configPath, 'utf8')
  const parsed = JSON.parse(raw)
  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    return { action: 'skipped', path: configPath, message: `${configLabel} must be an object; capability declarations not added` }
  }

  let changed = false
  const nextv = { ...parsed }
  const requiresMap = nextv.requires && typeof nextv.requires === 'object' && !Array.isArray(nextv.requires)
    ? { ...nextv.requires }
    : {}
  const modulesMap = nextv.modules && typeof nextv.modules === 'object' && !Array.isArray(nextv.modules)
    ? { ...nextv.modules }
    : {}

  if (JSON.stringify(requiresMap[capabilityName] ?? null) !== JSON.stringify(desiredRequire)) {
    requiresMap[capabilityName] = desiredRequire
    changed = true
  }

  if (JSON.stringify(modulesMap[capabilityName] ?? null) !== JSON.stringify(desiredModule)) {
    modulesMap[capabilityName] = desiredModule
    changed = true
  }

  if (!changed) {
    return { action: 'unchanged', path: configPath }
  }

  nextv.requires = requiresMap
  nextv.modules = modulesMap
  writeFileSync(configPath, `${JSON.stringify(nextv, null, 2)}\n`, 'utf8')
  return { action: 'updated', path: configPath }
}

